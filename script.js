let current = [];
let index = 0;
let score = 0;
let wrong = [];
let time = 3600;
let timer;

function shuffle(arr) {
  for (let i = arr.length - 1; i > 0; i--) {
    let j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

function startSection(name) {
current = JSON.parse(JSON.stringify(sections[name]));
current.forEach(q => q.options = shuffle(q.options));

index = 0;
score = 0;
wrong = [];
time = 3600;

document.getElementById("menu").classList.add("hidden");
document.getElementById("quiz").classList.remove("hidden");

startTimer();
loadQuestion();
}

function loadQuestion() {
if (index >= current.length) {
finishSection();
return;
}

let q = current[index];
document.getElementById("question").innerText = q.q;
document.getElementById("feedback").innerText = "";

let optDiv = document.getElementById("options");
optDiv.innerHTML = "";

q.options.forEach(opt => {
let btn = document.createElement("button");
btn.innerText = opt;
btn.onclick = () => checkAnswer(opt);
optDiv.appendChild(btn);
});
}

function checkAnswer(selected) {
let correct = current[index].answer;
let feedback = document.getElementById("feedback");

if (selected === correct) {
score += 4;
feedback.innerText = "Correct!";
feedback.className = "correct";
} else {
score -= 1;
wrong.push(current[index]);
feedback.innerText = "Wrong! Correct: " + correct;
feedback.className = "wrong";
}

document.querySelectorAll("#options button").forEach(b => b.disabled = true);
}

function nextQuestion() {
index++;
loadQuestion();
}

function skipQuestion() {
wrong.push(current[index]);
index++;
loadQuestion();
}

function finishSection() {
  clearInterval(timer);

  // 🔁 Repeat wrong questions first
  if (wrong.length > 0) {
    current = wrong;
    wrong = [];
    index = 0;
    alert("Repeating wrong questions...");
    loadQuestion();
    return;
  }

  // 📊 Calculate results
  let total = current.length;
  let correct = Math.floor((score + total) / 5); 
  // (+4 correct, -1 wrong logic reversed)

  let incorrect = total - correct;

  // 🟢 Display result
  document.getElementById("quiz").innerHTML =
    "<h2>Section Completed ✅</h2>" +
    "<h3>Score: " + score + "</h3>" +
    "<p>Total Questions: " + total + "</p>" +
    "<p>Correct Answers: " + correct + "</p>" +
    "<p>Wrong Answers: " + incorrect + "</p>" +
    "<br><button onclick='location.reload()'>Back to Menu</button>";
}
